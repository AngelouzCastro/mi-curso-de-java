    public void buscarcancion(String texto, String buscado) {
        try {

            Conectar cc = new Conectar();
            Connection cn = cc.conexion();
            Statement sent;
            String sql;

            String filtro = "%" + texto + "%";
            String[] titulo = {"Numero", "Titulo", "Autor", "Artista", "Genero"};
            sql = "SELECT * FROM idconexion WHERE " + buscado + " LIKE" + '"' + filtro + '"';
            DefaultTableModel model = new DefaultTableModel(null, titulo);
            sent = cn.prepareStatement(sql);
            ResultSet re = sent.executeQuery(sql);
            String[] fila = new String[30];
            while (re.next()) {
                fila[0] = re.getString("numero");
                fila[1] = re.getString("titulo");
                fila[2] = re.getString("autor");
                fila[3] = re.getString("artista");
                fila[4] = re.getString("genero");
                model.addRow(fila);
            }
            jTable1.setModel(model);
            re.close();
            sent.close();
        } catch (Exception e) {
            //System.err.println(""+e.getMessage());
        }
    }

