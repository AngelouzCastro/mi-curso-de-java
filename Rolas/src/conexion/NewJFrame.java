    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Conectar cc = new Conectar();
        Connection cn = cc.conexion();
        try {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO idconexion(numero,titulo,autor,artista,genero) VALUES(?,?,?,?,?)");
            pst.setString(1, TxtNumero.getText());
            pst.setString(2, TxtTitulo.getText());
            pst.setString(3, TxtAutor.getText());
            pst.setString(4, TxtArtista.getText());
            pst.setString(5, TxtGenero.getText());

            int a = pst.executeUpdate();
            if (a > 0) {
                JOptionPane.showMessageDialog(null, "Registro exitoso");
                mostrardatos("");
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al agregar");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

